package cn.ym.pojo;

public class nodes {
 private Integer NodeId;
 private Integer NodeParentId;
 private Integer NodeLevel;
 private Integer NodeOrder;
 private Integer Count;
 private String NodeName;
 private String IsHaveChild;
 private String IsVisible; 
 private String NodeCode;
public Integer getNodeId() {
	return NodeId;
}
public void setNodeId(Integer nodeId) {
	NodeId = nodeId;
}
public Integer getNodeParentId() {
	return NodeParentId;
}
public void setNodeParentId(Integer nodeParentId) {
	NodeParentId = nodeParentId;
}
public Integer getNodeLevel() {
	return NodeLevel;
}
public void setNodeLevel(Integer nodeLevel) {
	NodeLevel = nodeLevel;
}
public Integer getNodeOrder() {
	return NodeOrder;
}
public void setNodeOrder(Integer nodeOrder) {
	NodeOrder = nodeOrder;
}
public Integer getCount() {
	return Count;
}
public void setCount(Integer count) {
	Count = count;
}
public String getNodeName() {
	return NodeName;
}
public void setNodeName(String nodeName) {
	NodeName = nodeName;
}
public String getIsHaveChild() {
	return IsHaveChild;
}
public void setIsHaveChild(String isHaveChild) {
	IsHaveChild = isHaveChild;
}
public String getIsVisible() {
	return IsVisible;
}
public void setIsVisible(String isVisible) {
	IsVisible = isVisible;
}
public String getNodeCode() {
	return NodeCode;
}
public void setNodeCode(String nodeCode) {
	NodeCode = nodeCode;
}
 
 
 
}
